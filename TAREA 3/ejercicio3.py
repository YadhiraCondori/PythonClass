print("Suma de numeros pares e impares")
suma_par = 0
suma_impar = 0
for i in range(100):
    print(i)
    if i % 2 == 0:
        suma_par = suma_par + i
    else:
        suma_impar = suma_impar + i
print("La suma de números pares es: "+ str(suma_par)) 
print("La suma de números impares es: "+ str(suma_impar))

    
    
        
           